    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $coupon = new Coupon();

        $coupon->code = trim($request->input('name')) ;
        $coupon->type = $request->input('type');
        if ($coupon->type==1){
            $coupon->value = $request->input('value');
        }elseif ($coupon->type==2){
            $coupon->percent = $request->input('value');
        }else{
            return redirect()->route('admin.coupon.index')->with('error','Tạo mã giảm giá thất bại');
        }

        $coupon->save();

        return redirect()->route('admin.coupon.index')->with('success','Thêm mã giảm thành công');


    }

